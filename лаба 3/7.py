from fractions import Fraction

frac1 = Fraction(4, 3)
frac2 = Fraction(5, 6)

# Выполняем операции
sum_res = frac1 + frac2
diff_res = frac1 - frac2
mul_res = frac1 * frac2
div_res = frac1 / frac2

# Выводим результаты
print(f'Сложение: {sum_res}')
print(f'Вычитание: {diff_res}')
print(f'Умножение: {mul_res}')
print(f'Деление: {div_res}')
